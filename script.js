$(document).ready(function(){

	$(".top-notification").click(function(){
		$(".top-notification").toggleClass("hide");
		})

	$(".show-cart-btn").click(function(){
		$(".cart-section").removeClass("hide-cart");
		$(".cart-section").addClass("show-cart");
	})

	$(".close-cart-btn").click(function(){
		$(".cart-section").removeClass("show-cart");
		$(".cart-section").addClass("hide-cart");
	})
	




	})

	